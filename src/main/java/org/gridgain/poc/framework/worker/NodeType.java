package org.gridgain.poc.framework.worker;

/**
 * Created by oostanin on 21.01.18.
 */
public enum NodeType {
    SERVER("server"),
    CLIENT("client"),
    ZOOKEEPER("zookeeper")
    ;

    private final String text;

    /**
     * @param text
     */
    NodeType(final String text) {
        this.text = text;
    }

    /* (non-Javadoc)
     * @see java.lang.Enum#toString()
     */
    @Override
    public String toString() {
        return text;
    }
}
